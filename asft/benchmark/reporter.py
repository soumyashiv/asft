"""
Benchmark Reporter — Generates JSON + HTML reports and comparison summaries.
Compares Full FT vs LoRA vs QLoRA vs Sparse vs ASFT across all metrics.
"""

from __future__ import annotations

import json
import logging
from pathlib import Path
from typing import Any

logger = logging.getLogger(__name__)


class BenchmarkReporter:
    """Generates rich HTML and JSON comparison reports from benchmark records."""

    def __init__(self, output_dir: str = "./asft_data/benchmarks"):
        self._output_dir = Path(output_dir)
        self._output_dir.mkdir(parents=True, exist_ok=True)

    def generate_html_report(
        self, records: list[dict[str, Any]], title: str = "ASFT Benchmark"
    ) -> str:
        """Generate a full HTML comparison report."""
        methods = sorted(set(r["method"] for r in records))
        metrics = [
            "accuracy",
            "training_time_seconds",
            "inference_time_ms",
            "peak_memory_mb",
            "hallucination_rate",
            "param_efficiency",
        ]

        # Compute averages per method
        stats: dict[str, dict[str, float]] = {}
        for method in methods:
            method_records = [r for r in records if r["method"] == method]
            stats[method] = {
                m: sum(r.get(m, 0) for r in method_records) / len(method_records) for m in metrics
            }

        # Build HTML
        rows_html = ""
        for method in methods:
            s = stats[method]
            rows_html += f"""
            <tr>
                <td class="method">{method}</td>
                <td class="metric">{s['accuracy']:.3f}</td>
                <td class="metric">{s['training_time_seconds']:.1f}s</td>
                <td class="metric">{s['inference_time_ms']:.1f}ms</td>
                <td class="metric">{s['peak_memory_mb']:.0f}MB</td>
                <td class="metric">{s['hallucination_rate']:.3f}</td>
                <td class="metric">{s['param_efficiency'] * 100:.1f}%</td>
            </tr>"""

        html = f"""<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>{title}</title>
<style>
  * {{ box-sizing: border-box; margin: 0; padding: 0; }}
  body {{ font-family: 'Segoe UI', system-ui, sans-serif; background: #0f172a; color: #e2e8f0; padding: 2rem; }}
  h1 {{ font-size: 2rem; font-weight: 700; color: #7c3aed; margin-bottom: 0.5rem; }}
  p.subtitle {{ color: #94a3b8; margin-bottom: 2rem; }}
  table {{ width: 100%; border-collapse: collapse; background: #1e293b; border-radius: 12px; overflow: hidden; box-shadow: 0 4px 24px rgba(0,0,0,0.3); }}
  th {{ background: #7c3aed; color: white; padding: 1rem; text-align: left; font-weight: 600; font-size: 0.85rem; text-transform: uppercase; letter-spacing: 0.05em; }}
  td {{ padding: 0.85rem 1rem; border-bottom: 1px solid #334155; }}
  td.method {{ font-weight: 700; color: #7c3aed; }}
  td.metric {{ color: #e2e8f0; font-variant-numeric: tabular-nums; }}
  tr:last-child td {{ border-bottom: none; }}
  tr:hover td {{ background: #273549; transition: background 0.15s; }}
  .asft-row td {{ color: #4ade80 !important; }}
  .badge {{ display: inline-block; padding: 0.2em 0.6em; border-radius: 4px; font-size: 0.75rem; font-weight: 600; }}
</style>
</head>
<body>
<h1>⚡ {title}</h1>
<p class="subtitle">Generated by ASFT Framework — Adaptive Sparse Fine-Tuning</p>
<table>
  <thead>
    <tr>
      <th>Method</th>
      <th>Accuracy ↑</th>
      <th>Train Time ↓</th>
      <th>Inference ↓</th>
      <th>Memory ↓</th>
      <th>Hallucination ↓</th>
      <th>Param Efficiency ↑</th>
    </tr>
  </thead>
  <tbody>
    {rows_html}
  </tbody>
</table>
<p style="margin-top:1rem; color:#475569; font-size:0.8rem;">↑ = higher is better, ↓ = lower is better</p>
</body>
</html>"""

        html_path = self._output_dir / "benchmark_report.html"
        with open(html_path, "w", encoding="utf-8") as f:
            f.write(html)

        logger.info("HTML report saved: %s", html_path)
        return str(html_path)

    def generate_json_report(self, records: list[dict[str, Any]]) -> str:
        """Save all records + summary as JSON."""
        json_path = self._output_dir / "benchmark_results.json"
        with open(json_path, "w") as f:
            json.dump({"records": records, "count": len(records)}, f, indent=2)
        logger.info("JSON report saved: %s", json_path)
        return str(json_path)

    def improvement_over_baseline(
        self, records: list[dict], baseline: str = "full_ft", target: str = "asft"
    ) -> dict[str, Any]:
        """Compute improvement % of target method over baseline."""
        baseline_recs = [r for r in records if r["method"] == baseline]
        target_recs = [r for r in records if r["method"] == target]

        if not baseline_recs or not target_recs:
            return {"error": f"Missing data for {baseline} or {target}"}

        def avg(recs, key):
            return sum(r.get(key, 0) for r in recs) / len(recs)

        metrics = ["accuracy", "training_time_seconds", "inference_time_ms", "peak_memory_mb"]
        improvements = {}
        for m in metrics:
            b, t = avg(baseline_recs, m), avg(target_recs, m)
            if b == 0:
                continue
            change = (b - t) / b  # positive = target is better (lower is better)
            if m == "accuracy":
                change = (t - b) / b  # higher is better
            improvements[m] = round(change * 100, 1)

        return {
            "baseline": baseline,
            "target": target,
            "improvements_percent": improvements,
        }
